package operations.adaptees;



public class AdvancedOperations {
    
    public float square(float a) {
        return a * a; //
    }
    
    public float cube(float a) {
        return a * a * a; //
    }
    
    public float half(float a) {
        return a / 2; //
    }
    
    public float multiplyByTwo(float a) {
        return a * 2; //
    }
}
